"""
utils/config.py — Конфигурация бота. Все чувствительные данные берутся из .env
"""

import os
from dotenv import load_dotenv

load_dotenv()  # загружаем переменные из файла .env


class BotConfig:
    # ──────────────────────────────────────────
    # Токен задаётся напрямую в bot.py → TOKEN
    # База данных: SQLite, файл data/modbot.db
    # (создаётся автоматически, PostgreSQL не нужен)
    # ──────────────────────────────────────────

    # ──────────────────────────────────────────
    # ID владельцев бота (могут использовать
    # команды владельца на любом сервере)
    # ──────────────────────────────────────────
    OWNER_IDS: set = {
        int(x) for x in os.getenv("OWNER_IDS", "").split(",") if x.strip().isdigit()
    }

    # ──────────────────────────────────────────
    # Лимиты системы предупреждений по умолчанию
    # (можно переопределить на каждом сервере)
    # ──────────────────────────────────────────
    DEFAULT_WARN_ACTIONS = {
        3: "mute_1h",    # 3 варна → мут 1 час
        5: "mute_24h",   # 5 варнов → мут 24 часа
        7: "kick",       # 7 варнов → кик
        10: "ban",       # 10 варнов → бан
    }

    # ──────────────────────────────────────────
    # Максимальное время мута (в секундах)
    # Discord ограничивает тайм-аут 28 днями
    # ──────────────────────────────────────────
    MAX_TIMEOUT_SECONDS: int = 2419200  # 28 дней

    # Цвета embed-сообщений
    COLOR_SUCCESS = 0x2ECC71   # зелёный
    COLOR_ERROR   = 0xE74C3C   # красный
    COLOR_WARNING = 0xF39C12   # оранжевый
    COLOR_INFO    = 0x3498DB   # синий
    COLOR_MOD     = 0x9B59B6   # фиолетовый (действия модерации)

    # Эмодзи для embed'ов
    EMOJI_BAN     = "🔨"
    EMOJI_KICK    = "👢"
    EMOJI_MUTE    = "🔇"
    EMOJI_WARN    = "⚠️"
    EMOJI_UNMUTE  = "🔊"
    EMOJI_UNBAN   = "✅"
    EMOJI_TICKET  = "🎫"
    EMOJI_LOG     = "📋"
    EMOJI_STATS   = "📊"
    EMOJI_SETTINGS = "⚙️"
